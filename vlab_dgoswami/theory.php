<div align="center" id="theory_text_heading">
<font face="Purisa"><b>1. Quantum Processes</b></font>
</div>

<div align="center" id="theory_text_style_1">
<font face="Purisa">Quantum properties dominate the fields of atomic and molecular physics. Radiation is quantized such 
that for a given frequency of radiation, there can be only one value of quantum energy for the photons of that radiation. 
The energy levels of atoms and molecules can have only certain quantized values. Transitions between these quantized states 
occur by the photon processes absorption, emission, and stimulated emission. All of these processes require that the photon 
energy given by the Planck relationship is equal to the energy separation of the participating pair of quantum energy states.
</font>
</div>
<div align="center"><img src="./theory files/qpro1.gif"></div>

