<a name="confocal"></a>

<div align="center">
<font face="Purisa" id="theory_text_heading"><b>6. Laser Scanning Confocal Microscopy</b></font>
</div>
<div align="center" id="theory_text_style_1">
<font face="Purisa">Confocal laser scanning microscopy (CLSM or LSCM) is a technique for obtaining high-resolution optical images with depth selectivity.[1] 
The key feature of confocal microscopy is its ability to acquire in-focus images from selected depths, a process known as optical sectioning. Images are acquired 
point-by-point and reconstructed with a computer, allowing three-dimensional reconstructions of topologically complex objects. For opaque specimens, this is useful 
for surface profiling, while for non-opaque specimens, interior structures can be imaged. For interior imaging, the quality of the image is greatly enhanced over 
simple microscopy because image information from multiple depths in the specimen is not superimposed. A conventional microscope "sees" as far into the specimen as 
the light can penetrate, while a confocal microscope only images one depth level at a time. In effect, the CLSM achieves a controlled and highly limited depth of 
focus. 
</font>
</div>
<div align="center">
<img src="./theory files/confocal.png" height="200px" width="400px">
</div>

<div align="center" id="theory_text_heading"><font face="Purisa">Principle 
of Confocal Microscopy</font>
</div>
<br>

<div align="center" id="theory_text_style_1">
<font face="Purisa">In a confocal laser scanning microscope, a laser beam passes through a light source aperture and then is focused by an objective lens into a 
small (ideally diffraction limited) focal volume within or on the surface of a specimen. In biological applications especially, the specimen may be fluorescent. 
Scattered and reflected laser light as well as any fluorescent light from the illuminated spot is then re-collected by the objective lens. A beam splitter separates 
off some portion of the light into the detection apparatus, which in fluorescence confocal microscopy will also have a filter that selectively passes the fluorescent 
wavelengths while blocking the original excitation wavelength. After passing a pinhole, the light intensity is detected by a photodetection device (usually a 
photomultiplier tube (PMT) or avalanche photodiode), transforming the light signal into an electrical one that is recorded by a computer. 
</font></div>
