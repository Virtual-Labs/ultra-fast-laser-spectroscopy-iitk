<a name="stemission"></a>

<div align="center" id="theory_text_heading">
<font face="Purisa"><b>2. Stimulated Emission </b></font></div>
<div align="center" id="theory_text_style_1"><font face="Purisa">If an electron is already in an excited state 
(an upper energy level, in contrast to its lowest possible level or "ground state"), then an incoming photon for which the quantum energy is equal to
 the energy difference between its present level and a lower level can "stimulate" a transition to that lower level, producing a second photon of the same energy. 
</font>
</div>

<div align="center">
<img src="./theory files/qpro2.gif">
</div>
<div align="center" id="theory_text_style_1">
<font face="Purisa">When a sizable population of electrons resides in upper levels, this condition is called a "population inversion", and it sets the stage for
 stimulated emission of multiple photons. This is the precondition for the light amplification which occurs in a laser, and since the emitted photons have a definite
 time and phase relation to each other, the light has a high degree of coherence. 
</font>
</div>

