<div align="center" id="theory_text_heading">
<font face="Purisa"><b>7. Z-Scan Techniques</b>
</font>
</div>
<div align="center" id="theory_text_style_1">
<font face="Purisa">In Nonlinear optics a z-scan measurement is used to measure the non-linear index n<sub>2</sub> 
Kerr nonlinearity and the non-linear absorption 
coefficient &amp;delta&amp;alpha via the "open" and "closed" methods respectively. As nonlinear absorption can affect the measurement of the non-linear index the 
open method is typically used in conjunction with the closed method to correct the calculated value. </font></div>
<div align="center"><img src="./theory files/zscan.
png" height="200px" width="700px">
</div>

<div align="center" id="theory_text_heading">
<font face="Purisa"><b>Zscan measurement diagram</b></font>
</div>
<br>

<div align="center" id="theory_text_style_1">
<font face="Purisa">Closed Aperture Z-scan Technique : In this setup an aperture is placed to prevent some of the light from reaching the detector. The equipment is 
arranged as can be seen in the diagram. A lens focuses a laser to a certain point, and after this point the beam naturally defocuses. After a further distance an 
aperture is placed with a detector behind it. The aperture causes only the central region of the cone of light to reach the detector. Typically values of the 
normalised transmittance are between 0.1 &lt; S &lt; 0.5. </font></div>
<div align="center" id="theory_text_style_1"><font face="Purisa">Open Aperture Z-scan Technique : This method is 
similar to the above method, however the aperture is removed or enlarged to allow all the light to reach the detector. This in effect sets the normalised 
transmittance to S = 1. This is used in order to measure the non-linear absorption coefficient &amp;delta&amp;alpha . 
</font>
</div>

