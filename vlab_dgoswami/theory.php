<a name="twolevel"></a>

<div align="center" id="theory_text_heading"><font face="Purisa"><b>5. Two level system and Population Inversion</b></font>
</div>
<div align="center" id="theory_text_style_1"><font face="Purisa">The two-level atom approximation. When the light angular frequency w coincides with one of the optical transitions of 
the atom, we have a resonant interaction between that transition and the light field. We can therefore neglect the other levels of the atom, which only weakly 
interact with the light because they are off-resonance.</font></div>
<div align="center"><img src="./theory files/2level.png" height="200px" width="400px">
</div>

<br>

<div align="center" id="theory_text_style_1">
<font face="Purisa">The atom will have many quantum levels, and there will be many possible optical transitions between them. However, in the two-level 
atom approximation we only consider the specific transition that satisfies eqn E<sub>2</sub>-E<sub>1</sub>= hw and ignore all the other levels. </font>
</div>
<div align="center" id="theory_text_style_1"><font face="Purisa">When the light pulse is in exact resonance with the 2 level system and the electric field amplitude of pulse is a 
constant then we observe Rabi oscillations (pure sinusoidal) in the system i.e. level 1 population and level 2 population oscillates between each other periodically. 
But when the electric field profile of pulse is Gaussian or Sech then we observe different population dynamics in the system. For a pulse whose area under the 
time-axis is a multiple of Pi, we observe complete population inversion of the 2 levels,for a pulse whose area under the time-axis is a multiple of Pi/2, 
we observe half population inversion of the 2 levels </font></div>
