<a name="trapping"></a>

<div align="center" id="theory_text_heading"><font face="Purisa"><b>8. Optical Trapping and Tweezers</b>
</font>
</div>

<div align="center" id="theory_text_style_1">><font face="Purisa">Optical tweezers (originally called "single-beam gradient force trap") are scientific instruments that use a highly-focused 
laser beam to provide an attractive or repulsive force (typically on the order of piconewtons), depending on the refractive index mismatch to physically hold and move
 microscopic dielectric objects.  </font>
</div>

<div align="center">
<img src="./theory files/tweezer.jpg" height="500px" width="700px">
</div>
<div align="center" id="theory_text_heading"><font face="Purisa">Optical tweezer diagram</font>
</div>
<br>
<div align="center" id="theory_text_style_1"><font face="Purisa">Optical tweezers are capable of manipulating nanometer and micrometer-sized dielectric particles by exerting 
extremely small
 forces via a highly focused laser beam. The beam is typically focused by sending it through a microscope objective. The narrowest point of the focused beam, known 
as the beam waist, contains a very strong electric field gradient. It turns out that dielectric particles are attracted along the gradient to the region of strongest 
electric field, which is the center of the beam. The laser light also tends to apply a force on particles in the beam along the direction of beam propagation. It is 
easy to understand why if one considers light to be a group of particles, each impinging on the tiny dielectric particle in its path. This is known as the scattering 
force and results in the particle being displaced slightly downstream from the exact position of the beam waist. </font>
</div>

